import java.util.Arrays;

public class OperatorDemo {
    public static void main (String [] args) {
        // Toán tử số học
        // + - * / %
        int a=7;
        int b=5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
